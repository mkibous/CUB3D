#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	size_t	l;
	char	*str;

	if (!s || !f)
		return (NULL);
	i = 0;
	l = ft_strlen(s);
	str = (char *)malloc(l + 1);
	if (str == NULL)
		return (NULL);
	while (s[i])
	{
		str[i] = (*f)(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
